def init_database():
    """初始化数据库"""
    print("Initializing database...")

    Base.metadata.create_all(bind=engine)

    db = SessionLocal()
    try:
        existing = db.query(City).first()
        if existing:
            print("Database already exists. To reinitialize, delete the database file first.")
            return

        # 导入城市数据
        from app.scripts.init_data import CITIES_DATA
        city_map = {}
        for city_data in CITIES_DATA:
            city = City(**city_data)
            db.add(city)
            city_map[city_data["name"]] = city

        db.commit()
        print(f"Added {len(CITIES_DATA)} cities")

        # 导入城市统计数据
        stats_count = 0
        for city_name, stats in CITY_STATS_2023.items():
            if city_name in city_map:
                city = city_map[city_name]
                stat = CityStats(
                    city_id=city.id,
                    year=2023,
                    per_gdp=stats[0],
                    per_income=stats[1],
                    house_price=stats[2],
                    population=stats[3],
                    gdp_total=stats[4]
                )
                db.add(stat)
                stats_count += 1

        db.commit()
        print(f"Added {stats_count} city statistics for 2023")

    except Exception as e:
        print(f"Error: {e}")
        db.rollback()
    finally:
        db.close()


if __name__ == "__main__":
    init_database()
